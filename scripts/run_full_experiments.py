#!/usr/bin/env python3
"""Unified experiment orchestration for the DynaPath-Lite paper.

Runs all experiment tiers sequentially and produces a unified comparison
table suitable for direct inclusion in the LaTeX manuscript.

Experiment tiers:
  1. Tabular baselines (HGB/Ridge/RF) — re-runs if --tabular is set
  2. Neural baselines (LSTM/Transformer/PathLLM-Static)
  3. DynaPathLLM variants (full/no_align/no_sd_align/concat/simple_gate/static_only)

Modes:
  quick  — smaller epochs, faster models, for smoke-testing the pipeline.
  full   — paper-ready settings.
  tabular-only  — only re-run tabular baselines.
  neural-only   — only neural baselines + DynaPathLLM variants.

Usage:
    # Quick smoke test of full pipeline:
    python scripts/run_full_experiments.py --mode quick

    # Full paper experiments:
    python scripts/run_full_experiments.py --mode full

    # Generate comparison table from existing results:
    python scripts/run_full_experiments.py --compare-only
"""

from __future__ import annotations

import argparse
import json
import subprocess
import sys
from pathlib import Path


PROJECT_ROOT = Path(__file__).resolve().parent.parent
DATA_DIR_DEFAULT = "data/processed/pkdd15_grid_120k_clean"
REPORTS_DIR = PROJECT_ROOT / "reports"


def regression_metrics(y_true, y_pred) -> dict[str, float]:
    import numpy as np
    y_true = np.asarray(y_true, dtype=float)
    y_pred = np.asarray(y_pred, dtype=float)
    err = np.abs(y_true - y_pred)
    return {
        "mae": float(err.mean()),
        "rmse": float(np.sqrt(np.mean((y_true - y_pred) ** 2))),
        "mape": float(np.mean(err / np.maximum(y_true, 1e-6))),
        "mare": float(err.sum() / max(float(np.sum(y_true)), 1e-6)),
    }


def run_script(script_name: str, args: list[str], description: str) -> bool:
    """Run a Python script and return True on success."""
    script_path = PROJECT_ROOT / "scripts" / script_name
    cmd = [sys.executable, str(script_path)] + args
    print(f"\n{'#'*70}")
    print(f"# {description}")
    print(f"# {' '.join(cmd)}")
    print(f"{'#'*70}\n")
    result = subprocess.run(cmd, cwd=str(PROJECT_ROOT))
    return result.returncode == 0


def load_json(path: Path) -> dict:
    with open(path, encoding="utf-8") as f:
        return json.load(f)


def collect_tabular_results() -> list[dict]:
    """Collect results from existing tabular experiment reports."""
    results = []
    # Try noleak report first
    noleak_path = REPORTS_DIR / "pkdd15_dynapath_lite_120k_noleak_metrics.json"
    if noleak_path.exists():
        report = load_json(noleak_path)
        for r in report.get("results", []):
            results.append({
                "tier": "tabular",
                "model": r["model"],
                "mae": r["mae"],
                "rmse": r["rmse"],
                "mape": r["mape"],
                "mare": r["mare"],
            })
    return results


def collect_neural_results() -> list[dict]:
    results = []
    path = REPORTS_DIR / "neural_baselines" / "baseline_metrics.json"
    if path.exists():
        report = load_json(path)
        for r in report.get("results", []):
            t = r["test"]
            results.append({
                "tier": "neural_baseline",
                "model": r["model"],
                "mae": t["mae"],
                "rmse": t["rmse"],
                "mape": t["mape"],
                "mare": t["mare"],
            })
    return results


def collect_variant_results() -> list[dict]:
    results = []
    path = REPORTS_DIR / "dynapath_variants" / "variant_metrics.json"
    if path.exists():
        report = load_json(path)
        for r in report.get("results", []):
            t = r["test"]
            results.append({
                "tier": "dynapath_variant",
                "model": f"DynaPathLLM-{r['variant']}",
                "mae": t["mae"],
                "rmse": t["rmse"],
                "mape": t["mape"],
                "mare": t["mare"],
            })
    return results


def generate_latex_table(all_results: list[dict], output_path: Path) -> str:
    """Generate a LaTeX-formatted comparison table."""
    lines = [
        "% Auto-generated by run_full_experiments.py",
        "\\begin{table}[htbp]",
        "  \\centering",
        "  \\caption{Full experimental comparison across model tiers.}",
        "  \\label{tab:full_comparison}",
        "  \\begin{tabular}{lcccc}",
        "    \\toprule",
        "    模型 & MAE/s & RMSE/s & MAPE & MARE \\\\",
        "    \\midrule",
    ]

    # Group by tier
    tiers: dict[str, list[dict]] = {}
    for r in all_results:
        tiers.setdefault(r["tier"], []).append(r)

    tier_labels = {
        "tabular": "表格特征模型",
        "neural_baseline": "神经序列基线",
        "dynapath_variant": "DynaPathLLM 变体",
    }

    for tier_key in ["tabular", "neural_baseline", "dynapath_variant"]:
        if tier_key not in tiers:
            continue
        lines.append(f"    \\multicolumn{{5}}{{c}}{{\\textbf{{{tier_labels[tier_key]}}}}} \\\\")
        lines.append("    \\midrule")
        for r in tiers[tier_key]:
            # bold the best result in each tier
            best_mae = min(x["mae"] for x in tiers[tier_key])
            is_best = abs(r["mae"] - best_mae) < 0.01
            model_str = f"\\textbf{{{r['model']}}}" if is_best else r["model"]
            lines.append(
                f"    {model_str} & {r['mae']:.2f} & {r['rmse']:.2f} & "
                f"{r['mape']:.4f} & {r['mare']:.4f} \\\\"
            )
        lines.append("    \\midrule")

    lines.extend([
        "  \\end{tabular}",
        "\\end{table}",
    ])

    latex = "\n".join(lines)
    output_path.write_text(latex, encoding="utf-8")
    return latex


def generate_fusion_comparison_table(variant_results: list[dict], output_path: Path) -> str:
    """Generate a focused table comparing fusion strategies."""
    # Extract only fusion-related variants
    fusion_keys = {
        "DynaPathLLM-static_only": "纯静态（无动态模态）",
        "DynaPathLLM-concat": "简单拼接融合",
        "DynaPathLLM-simple_gate": "普通门控融合（无可信度）",
        "DynaPathLLM-full": "可靠性感知门控融合（完整模型）",
    }

    fusion_rows = [r for r in variant_results if r["model"] in fusion_keys]
    if not fusion_rows:
        return ""

    static_mae = next(
        (r["mae"] for r in fusion_rows if r["model"] == "DynaPathLLM-static_only"), None
    )

    lines = [
        "% Auto-generated fusion strategy comparison",
        "\\begin{table}[htbp]",
        "  \\centering",
        "  \\caption{融合策略对比：可靠性感知门控 vs. 简单融合}",
        "  \\label{tab:fusion_comparison}",
        "  \\begin{tabular}{lcccc}",
        "    \\toprule",
        "    融合策略 & MAE/s & RMSE/s & MAPE & MARE \\\\",
        "    \\midrule",
    ]

    best_mae = min(r["mae"] for r in fusion_rows)
    for r in fusion_rows:
        label = fusion_keys.get(r["model"], r["model"])
        is_best = abs(r["mae"] - best_mae) < 0.01
        model_str = f"\\textbf{{{label}}}" if is_best else label
        lines.append(
            f"    {model_str} & {r['mae']:.2f} & {r['rmse']:.2f} & "
            f"{r['mape']:.4f} & {r['mare']:.4f} \\\\"
        )

    lines.extend([
        "    \\bottomrule",
        "  \\end{tabular}",
        "\\end{table}",
    ])
    latex = "\n".join(lines)
    output_path.write_text(latex, encoding="utf-8")
    return latex


def generate_ablation_table(variant_results: list[dict], output_path: Path) -> str:
    """Generate architecture ablation table."""
    ablation_order = [
        ("DynaPathLLM-full", "完整模型（含 TS+SD 对齐）"),
        ("DynaPathLLM-no_align", "无对齐损失（$\\lambda_{TS}=\\lambda_{SD}=0$）"),
        ("DynaPathLLM-no_sd_align", "无静态-动态对齐（$\\lambda_{SD}=0$）"),
    ]

    ablation_rows = []
    for model_key, label in ablation_order:
        for r in variant_results:
            if r["model"] == model_key:
                ablation_rows.append((label, r))
                break

    if not ablation_rows:
        return ""

    lines = [
        "% Auto-generated architecture ablation",
        "\\begin{table}[htbp]",
        "  \\centering",
        "  \\caption{模型架构消融：层次化跨模态对齐的贡献}",
        "  \\label{tab:arch_ablation}",
        "  \\begin{tabular}{lcccc}",
        "    \\toprule",
        "    模型配置 & MAE/s & RMSE/s & MAPE & MARE \\\\",
        "    \\midrule",
    ]

    best_mae = min(r["mae"] for _, r in ablation_rows)
    for label, r in ablation_rows:
        is_best = abs(r["mae"] - best_mae) < 0.01
        model_str = f"\\textbf{{{label}}}" if is_best else label
        lines.append(
            f"    {model_str} & {r['mae']:.2f} & {r['rmse']:.2f} & "
            f"{r['mape']:.4f} & {r['mare']:.4f} \\\\"
        )

    lines.extend([
        "    \\bottomrule",
        "  \\end{tabular}",
        "\\end{table}",
    ])
    latex = "\n".join(lines)
    output_path.write_text(latex, encoding="utf-8")
    return latex


def main() -> None:
    parser = argparse.ArgumentParser(description="Unified experiment runner")
    parser.add_argument("--data-dir", default=DATA_DIR_DEFAULT)
    parser.add_argument("--mode", default="quick",
                        choices=["quick", "full", "tabular-only", "neural-only"])
    parser.add_argument("--compare-only", action="store_true",
                        help="Only generate comparison tables from existing results.")
    parser.add_argument("--no-llm", action="store_true", default=True,
                        help="Use small Transformer instead of GPT-2 (default for quick mode).")
    parser.add_argument("--with-llm", action="store_true",
                        help="Use actual GPT-2 (overrides --no-llm).")
    args = parser.parse_args()

    # Mode settings
    if args.mode == "quick":
        epochs = 5
        batch_size = 16
        models = "lstm,transformer,pathllm"
        variants = "full,static_only"
    elif args.mode == "full":
        epochs = 20
        batch_size = 16
        models = "lstm,transformer,pathllm"
        variants = "full,no_align,no_sd_align,concat,simple_gate,static_only"
    elif args.mode == "tabular-only":
        epochs = 0
        batch_size = 0
        models = ""
        variants = ""
    else:  # neural-only
        epochs = 20
        batch_size = 16
        models = "lstm,transformer,pathllm"
        variants = "full,no_align,no_sd_align,concat,simple_gate,static_only"

    use_llm = args.with_llm and not args.no_llm
    no_llm_flag = [] if use_llm else ["--no-llm"]

    if args.compare_only:
        print("Collecting existing results for comparison...")
    else:
        # ---- Tier 1: Tabular baselines (re-run if not compare-only) ----
        if args.mode not in ("neural-only",):
            run_script(
                "train_pkdd15_dynapath_lite.py",
                [
                    f"--data-dir={args.data_dir}",
                    "--report=reports/pkdd15_dynapath_lite_120k_noleak_metrics.json",
                    "--model-dir=reports/pkdd15_dynapath_lite_120k_noleak_models",
                ],
                "Tier 1: Tabular baselines (Ridge, HGB, RF)",
            )

            run_script(
                "run_pkdd15_ablation.py",
                [
                    f"--data-dir={args.data_dir}",
                    "--report=reports/pkdd15_dynapath_lite_120k_ablation_metrics.json",
                    "--markdown=reports/pkdd15_dynapath_lite_120k_ablation_report.md",
                ],
                "Tier 1: Dynamic feature ablation",
            )

        # ---- Tier 2: Neural baselines ----
        if models:
            run_script(
                "train_neural_baselines.py",
                [
                    f"--data-dir={args.data_dir}",
                    "--output-dir=reports/neural_baselines",
                    f"--models={models}",
                    f"--epochs={epochs}",
                    f"--batch-size={batch_size}",
                ] + no_llm_flag,
                f"Tier 2: Neural baselines ({models})",
            )

        # ---- Tier 3: DynaPathLLM variants ----
        if variants:
            run_script(
                "train_dynapath_variants.py",
                [
                    f"--data-dir={args.data_dir}",
                    "--output-dir=reports/dynapath_variants",
                    f"--variant={variants}",
                    f"--epochs={epochs}",
                    f"--batch-size={batch_size}",
                ] + no_llm_flag,
                f"Tier 3: DynaPathLLM variants ({variants})",
            )

    # ---- Collect all results and generate comparison tables ----
    all_results = []
    all_results.extend(collect_tabular_results())
    all_results.extend(collect_neural_results())
    all_results.extend(collect_variant_results())

    if not all_results:
        print("No results found. Run experiments first or check reports/ directory.")
        return

    print(f"\nCollected {len(all_results)} result entries.")

    # Print unified table
    print("\n" + "="*72)
    print("UNIFIED EXPERIMENT COMPARISON")
    print("="*72)
    print(f"{'Model':<32s} {'MAE/s':>8s} {'RMSE/s':>8s} {'MAPE':>8s} {'MARE':>8s}")
    print("-"*72)
    for r in all_results:
        print(f"{r['model']:<32s} {r['mae']:>8.2f} {r['rmse']:>8.2f} "
              f"{r['mape']:>8.4f} {r['mare']:>8.4f}")

    # Generate LaTeX tables
    tex_dir = REPORTS_DIR / "tables"
    tex_dir.mkdir(parents=True, exist_ok=True)

    full_table = generate_latex_table(all_results, tex_dir / "full_comparison.tex")
    print(f"\nFull comparison table: {tex_dir / 'full_comparison.tex'}")

    variant_results = collect_variant_results()
    if variant_results:
        fusion_table = generate_fusion_comparison_table(
            variant_results, tex_dir / "fusion_comparison.tex"
        )
        if fusion_table:
            print(f"Fusion comparison table: {tex_dir / 'fusion_comparison.tex'}")

        ablation_table = generate_ablation_table(
            variant_results, tex_dir / "arch_ablation.tex"
        )
        if ablation_table:
            print(f"Architecture ablation table: {tex_dir / 'arch_ablation.tex'}")

    # Save unified JSON
    unified_path = REPORTS_DIR / "full_experiment_comparison.json"
    unified_path.write_text(
        json.dumps(all_results, indent=2, ensure_ascii=False), encoding="utf-8"
    )
    print(f"\nUnified results: {unified_path}")


if __name__ == "__main__":
    main()
